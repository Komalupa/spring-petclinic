CREATE TABLE IF NOT EXISTS pet_attributes (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  pet_id INTEGER NOT NULL UNIQUE,
  temperament VARCHAR(100),
  length DOUBLE,
  weight DOUBLE,
  color VARCHAR(50),
  CONSTRAINT fk_pet_attr_pet FOREIGN KEY (pet_id) REFERENCES pets(id)
);
